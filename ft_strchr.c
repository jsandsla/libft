#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	ch;

	ch = (char)c;
	while (*s != '\0' && *s != ch)
	{
		s += 1;
	}
	return (*s == ch ? (char *)s : NULL);
}
