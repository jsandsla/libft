#include "libft.h"

void	ft_transpose_m3(t_m3 l)
{
	t_m3	t;

	t[0][1] = l[1][0];
	t[0][2] = l[2][0];
	t[1][0] = l[0][1];
	t[1][2] = l[2][1];
	t[2][0] = l[0][2];
	t[2][1] = l[1][2];
	l[0][1] = t[0][1];
	l[0][2] = t[0][2];
	l[1][0] = t[1][0];
	l[1][2] = t[1][2];
	l[2][0] = t[2][0];
	l[2][1] = t[2][1];
}

void	ft_transpose_m3_to(t_m3 l, t_m3 out)
{
	out[0][0] = l[0][0];
	out[0][1] = l[1][0];
	out[0][2] = l[2][0];
	out[1][0] = l[0][1];
	out[1][1] = l[1][1];
	out[1][2] = l[2][1];
	out[2][0] = l[0][2];
	out[2][1] = l[1][2];
	out[2][2] = l[2][2];
}

void	ft_transpose_m4(t_m4 l)
{
	t_m4	m4;

	ft_transpose_m4_to(l, m4);
	ft_copy_m4(m4, l);
}

void	ft_transpose_m4_to(t_m4 l, t_m4 out)
{
	out[0][0] = l[0][0];
	out[0][1] = l[1][0];
	out[0][2] = l[2][0];
	out[0][3] = l[3][0];
	out[1][0] = l[0][1];
	out[1][1] = l[1][1];
	out[1][2] = l[2][1];
	out[1][3] = l[3][1];
	out[2][0] = l[0][2];
	out[2][1] = l[1][2];
	out[2][2] = l[2][2];
	out[2][3] = l[3][2];
	out[3][0] = l[0][3];
	out[3][1] = l[1][3];
	out[3][2] = l[2][3];
	out[3][3] = l[3][3];
}
