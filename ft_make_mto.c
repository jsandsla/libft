#include "libft.h"

void	ft_make_m4_from_m3(t_m3 m, t_m4 out)
{
	ft_identity_m4(out);
	out[0][0] = m[0][0];
	out[0][1] = m[0][1];
	out[0][2] = m[0][2];
	out[1][0] = m[1][0];
	out[1][1] = m[1][1];
	out[1][2] = m[1][2];
	out[2][0] = m[2][0];
	out[2][1] = m[2][1];
	out[2][2] = m[2][2];
}

void	ft_make_m3_from_m4(t_m4 m, t_m3 out)
{
	out[0][0] = m[0][0];
	out[0][1] = m[0][1];
	out[0][2] = m[0][2];
	out[1][0] = m[1][0];
	out[1][1] = m[1][1];
	out[1][2] = m[1][2];
	out[2][0] = m[2][0];
	out[2][1] = m[2][1];
	out[2][2] = m[2][2];
}
